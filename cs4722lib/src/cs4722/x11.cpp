GLfloat* color::as_float_array() const {
    return new GLfloat[4]{static_cast<float>(r) / 255.0f, static_cast<float>(g) / 255.0f,
                          static_cast<float>(b) / 255.0f, static_cast<float>(a) / 255.0f };
}


std::unique_ptr<GLfloat[]> color::as_float() const {
    return std::unique_ptr<GLfloat[]>(as_float_array());
}

void color::as_float(GLfloat* color) const {
    color[0] = static_cast<float>(r) / 255.0f;
    color[1]  = static_cast<float>(g) / 255.0f;
    color[2] =  static_cast<float>(b) / 255.0f;
    color[3] = static_cast<float>(a) / 255.0f;
}



glm::vec4* color::as_vec4() const {
    return new glm::vec4(static_cast<float>(r) / 255.0f, static_cast<float>(g) / 255.0f,
                          static_cast<float>(b) / 255.0f, static_cast<float>(a) / 255.0f );
}

